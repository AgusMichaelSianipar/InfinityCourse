    private void btnDaftar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftar1ActionPerformed
        
        if(!textNim.getText().isEmpty() &&
              !textNamaMahasiswa.getText().isEmpty() &&
                      !textJurusan.getText().isEmpty() &&
                !textSemester.getText().isEmpty()
           ){
        
                 try {
                   int semester = Integer.parseInt(textSemester.getText());

                    Mahasiswa mhs = new Mahasiswa();
                    mhs.setNim(textNim.getText());
                    mhs.setNama(textNamaMahasiswa.getText());
                    mhs.setJurusan(textJurusan.getText());
                    mhs.setJk(comboJk.getSelectedItem().toString());
                    mhs.setSemester(semester);

                    DaoMahasiswa dm = new DaoMahasiswa();
                    dm.setMahasiswa(mhs);
                    dm.insert(Tabel);
                    dm.fillCombo(comboKelas1);

                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "SEMESTER HARUS ANGKA");
                }
        }
    }//GEN-LAST:event_btnDaftar1ActionPerformed

