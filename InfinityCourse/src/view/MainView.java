    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed


            try {
                String nim = comboKelas1.getSelectedItem().toString().substring(1, 9);
                
                //tanggal haris ini
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                LocalDate localDate = LocalDate.now();
                
                //comboPengajar bersisi ([id 4 digit]) + nama pengajar
                //untuk mendapatkan id saja maka string harus 
                //di potong menggunakan substring dari index 1 sd 5
                int id = Integer.parseInt(comboPengajar.getSelectedItem().toString().substring(1, 5));
                
                Transaksi trs = new Transaksi();
                trs.setNim(nim);
                trs.setIdPengajar(id);
                trs.setTglDaftar(dtf.format(localDate));

                DaoTransaksi dt = new DaoTransaksi();
                dt.setTransaksi(trs);
                dt.insert();

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "ID Harus angka!");
            }

    }//GEN-LAST:event_btnDaftarActionPerformed

